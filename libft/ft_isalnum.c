#include "libft.h"

int	ft_isalnum(int arg)
{
	if ('A' <= arg && arg <= 'Z')
		return (1);
	else if ('a' <= arg && arg <= 'z')
		return (1);
	else if ('0' <= arg && arg <= '9')
		return (1);
	return (0);
}
